void mode_off_begin() {
    OUT::led_Debug.color = CRGB::Gray;
    OUT::led_Debug.needsUpdate = false;
    OUT::led_O.color = CRGB::Black;
    OUT::led_O.needsUpdate = true;
    OUT::led_X.color = CRGB::Black;
    OUT::led_X.needsUpdate = true;
    OUT::motorAcc.motEn = false;
}


namespace Mode {

    void off() {
        if(ST::state.mode_previous != ST::Types::mode_t::Off) { mode_off_begin();}
        
        OUT::display.needsUpdate = true;

        if(ST::offMenu.screen == ST::Types::screen_t::Main) {
            _drawScreen_0();
        } else if (ST::offMenu.screen == ST::Types::screen_t::Settings) {
            _drawScreen_1();
        }
    } 
}